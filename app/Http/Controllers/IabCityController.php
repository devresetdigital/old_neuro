    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $cities=[];
        if(strlen($_GET["q"])>3) {
            $cities = IabCity::where('city', 'LIKE','%'. $_GET["q"] . '%')->take(50)->get();
        }
        $c=0;
        $data["results"] = [];
        foreach ($cities as $val) {
            $data["results"][$c]["id"] = $val["country"]."-".$val["code"];
            $data["results"][$c]["text"] = $val["city"]." (".$val["pid"].") (".$val["country"].")";
            $c++;
        }
        return $data;
    }

